export const EXTREME_TENSION_SAFETY_SOP = `
# Extreme Tension Safety
*(Standard Operating Procedures for Spring Handling)*

## The "Cone of Fire"
Never stand directly in front of a winding cone or in the path of the winding bars. Always position your body to the side of the spring line.

## Winding Bar Protocol
1. **Never use screwdrivers**: Only use tool-grade winding bars sized for the cone.
2. **Double-Bar Rule**: One bar must ALWAYS be fully engaged and held while the other is being inserted.
3. **Engagement**: Ensure the bar is inserted to the full depth of the hole. Clean out debris first.

## Clamping & Latching
1. **Vise-Grips**: Must be used on the shaft when adjusting drums to prevent the shaft from spinning under load.
2. **Door Clamps**: Secure the door to the track when working on springs to prevent accidental opening.

## Set Screw Safety
1. **Dimpling**: Do not over-torque set screws. 1/2 to 3/4 turn past finger-tight is standard.
2. **Inspection**: Inspect the shaft for "burrs" or damage before adjusting. File down if necessary to prevent the cone from sticking.

## Emergency Scenarios
- **Snapped Bar**: If a bar snaps or slips, do not try to catch it. Step back immediately. 
- **Cable Jump**: If a cable jumps, close the door immediately (if safe) or use multiple clamps to secure it before investigating.
`;

export const TROUBLESHOOTING_SPRINGS_SOP = `
# Troubleshooting Springs

## Visually Inspect the Springs
- **Coil Separation**: Inspect for separation between coils indicating a break.
- **Condition**: Check for excessive rust, oil burn-off, stretching, or uneven spacing.
- **Cones**: Confirm winding cones are secure and set screws are properly seated on the shaft.
- **Stationary Cones**: Verify they are tightly fastened to the spring anchor plate.

## Check the Balance
1. Disconnect the operator and manually lift the door.
2. Raise the door to mid-travel and release.
- **Stays in place**: Correct balance.
- **Drops**: Under-sprung.
- **Rises**: Over-sprung.

## Check the Cable Length
- **Fully Open**: Inspect lift cables at the drums. Ensure no slack is present.
- **Tension**: Confirm cables are evenly tensioned and seated in grooves.
- **Closed Position**: Wrap should be ½ to ¾ around the drum.
- *Uneven tension may indicate improper spring torque or cable length mismatch.*

## Check the Specs of the Door
- Confirm door height and width.
- Verify track radius and drum size/type.
- Inspect for added weight (struts, insulation, glass) affecting required torque.

## Measure the Springs
- Measure **Inside Diameter**.
- Measure **Overall Relaxed Length**.
- Measure **Coils with a spring gauge**.
- Confirm Left-Wound and Right-Wound orientation matches the installation side.

## Weigh the Door
- Disconnect springs safely from tension.
- Use a scale at the center of the bottom section.
- Ensure full load is on the scale with cables properly routed.

## Check Shaft and Bearing Function
- Inspect torsion shaft for level.
- Ensure end bearings and center bearings rotate freely.
- *Binding in bearings can create "false balance" issues.*

## Verify Spring Turns
- Apply the correct winding formula based on drum diameter and door height.
- Confirm installed turns match calculated requirements.

## Use the SSC Engineering App
- Input door weight, height, track radius, drum size, and cycle life.
- Verify recommended spring IPPT and torque output.
- Compare calculated specs with installed springs.

## Final Balance Check
- Reapply correct turns and cycle the door manually.
- Confirm the door holds at mid-travel.
- **IMPORTANT**: If tension was adjusted, reset operator limits and forces.
`;

